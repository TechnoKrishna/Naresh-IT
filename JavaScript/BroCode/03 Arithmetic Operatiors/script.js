let a = 31;

a = a + 1;
console.log(a); // 32

a = a - 1;
console.log(a); // 31

a = a % 2;
console.log(a); // 1

a = a * 10;
console.log(a); // 10

a = a / 2;
console.log(a); // 5

a++;
console.log(a); // 6

a--;
console.log(a); // 5

a = a ** 3;
console.log(a); // 125

