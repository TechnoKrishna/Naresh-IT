let x = 5; // assigns 5 to x

console.log(5 == "5"); // true (same value after conversion)
console.log(true == 1); // true (true becomes 1)

console.log(5 === "5"); // false (number vs string)
console.log(5 === 5); // true

console.log(5 != "5"); // false (same value after conversion)
console.log(5 != 6); // true

console.log(5 !== "5"); // true (different type)
console.log(5 !== 5); // false (same value and type)
