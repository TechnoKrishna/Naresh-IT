public class StringConcatenation {
    public static void main(String[] args) {
        int x = 100;
        int y = 200;

        System.out.println("Sum of x and y is: " + x + y);
        System.out.println(+x + y);
        System.out.println("" + x + y);
        System.out.println("Sum of : " + (x + y));
        System.out.println(40 + 40 + " NIT " + 80 + 80);
    }
}
