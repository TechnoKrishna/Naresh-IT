#include <stdio.h>
#include <conio.h>

int main() {

    // C DATA TYPES CYCLES


    // SIGNED ==> -128 TO +127
    // UNSIGNED ==> 0 TO 255

    char a = 65, b = 66, c = a + b;

    printf("c = %d\n", c);
    printf("c = %d\n", c=260);
    printf("c = %d\n", c=-260);

    // a =  65
    // b = +66
    // c = 131

    //   256
    //  -131
    // =-125
    
    //--------------------------

    return 0;
}